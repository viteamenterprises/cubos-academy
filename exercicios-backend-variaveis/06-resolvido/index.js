
// Calcular velocidade média e converter o valor para km/h

let distPercorrida = 500;
let tempo = 10;
let velocidadeMS = distPercorrida / tempo;
// Conversão do valor em km/h
let velocidadeKM = velocidadeMS * 3.6;
console.log(`Sua velocidade média do percurso foi de ${velocidadeKM} Km/H`);

